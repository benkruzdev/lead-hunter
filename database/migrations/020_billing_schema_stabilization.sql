-- ============================================================================
-- 4. SYSTEM SETTINGS — credit rate and API key fields
-- ============================================================================
-- Runtime billing and search code reads these columns from system_settings id=1.
-- They must exist for the system to function correctly at boot.

DO $$
BEGIN
    -- Credit rate per record returned from Google Maps (used in search billing)
    -- Named credits_per_page for backwards compatibility with existing code comment.
    IF NOT EXISTS (SELECT 1 FROM information_schema.columns
                   WHERE table_schema = 'public'
                   AND table_name = 'system_settings'
                   AND column_name = 'credits_per_page') THEN
        ALTER TABLE public.system_settings ADD COLUMN credits_per_page INTEGER NOT NULL DEFAULT 1;
    END IF;

    -- Credit rate per enrichment operation (phone/email lookup)
    IF NOT EXISTS (SELECT 1 FROM information_schema.columns
                   WHERE table_schema = 'public'
                   AND table_name = 'system_settings'
                   AND column_name = 'credits_per_enrichment') THEN
        ALTER TABLE public.system_settings ADD COLUMN credits_per_enrichment INTEGER NOT NULL DEFAULT 1;
    END IF;

    -- Legacy alias: credits_per_lead (same concept, kept for any older admin code)
    IF NOT EXISTS (SELECT 1 FROM information_schema.columns
                   WHERE table_schema = 'public'
                   AND table_name = 'system_settings'
                   AND column_name = 'credits_per_lead') THEN
        ALTER TABLE public.system_settings ADD COLUMN credits_per_lead INTEGER NOT NULL DEFAULT 1;
    END IF;

    -- Google Maps Places API key (required for search functionality)
    IF NOT EXISTS (SELECT 1 FROM information_schema.columns
                   WHERE table_schema = 'public'
                   AND table_name = 'system_settings'
                   AND column_name = 'google_maps_api_key') THEN
        ALTER TABLE public.system_settings ADD COLUMN google_maps_api_key TEXT;
    END IF;

    -- SMTP configuration (used by email/notification systems)
    IF NOT EXISTS (SELECT 1 FROM information_schema.columns
                   WHERE table_schema = 'public'
                   AND table_name = 'system_settings'
                   AND column_name = 'smtp_host') THEN
        ALTER TABLE public.system_settings ADD COLUMN smtp_host TEXT;
    END IF;

    IF NOT EXISTS (SELECT 1 FROM information_schema.columns
                   WHERE table_schema = 'public'
                   AND table_name = 'system_settings'
                   AND column_name = 'smtp_port') THEN
        ALTER TABLE public.system_settings ADD COLUMN smtp_port INTEGER NOT NULL DEFAULT 587;
    END IF;

    IF NOT EXISTS (SELECT 1 FROM information_schema.columns
                   WHERE table_schema = 'public'
                   AND table_name = 'system_settings'
                   AND column_name = 'smtp_user') THEN
        ALTER TABLE public.system_settings ADD COLUMN smtp_user TEXT;
    END IF;

    IF NOT EXISTS (SELECT 1 FROM information_schema.columns
                   WHERE table_schema = 'public'
                   AND table_name = 'system_settings'
                   AND column_name = 'smtp_pass') THEN
        ALTER TABLE public.system_settings ADD COLUMN smtp_pass TEXT;
    END IF;

    IF NOT EXISTS (SELECT 1 FROM information_schema.columns
                   WHERE table_schema = 'public'
                   AND table_name = 'system_settings'
                   AND column_name = 'smtp_from') THEN
        ALTER TABLE public.system_settings ADD COLUMN smtp_from TEXT;
    END IF;
END $$;


-- ============================================================================
-- 5. CREDIT LEDGER — order reference alignment
-- ============================================================================
-- credit_ledger tracks all credit movements. Order completion writes rows here
-- without an order_id FK (compatibility). Adding the optional reference column
-- moves us toward better traceability without breaking existing rows.

DO $$
BEGIN
    IF NOT EXISTS (SELECT 1 FROM information_schema.columns
                   WHERE table_schema = 'public'
                   AND table_name = 'credit_ledger'
                   AND column_name = 'order_id') THEN
        ALTER TABLE public.credit_ledger ADD COLUMN order_id UUID REFERENCES public.orders(id) ON DELETE SET NULL;
    END IF;

    -- Source label: 'billing' | 'search' | 'export' | 'admin_adjust' | 'admin_grant'
    IF NOT EXISTS (SELECT 1 FROM information_schema.columns
                   WHERE table_schema = 'public'
                   AND table_name = 'credit_ledger'
                   AND column_name = 'source') THEN
        ALTER TABLE public.credit_ledger ADD COLUMN source TEXT;
    END IF;
END $$;

CREATE INDEX IF NOT EXISTS idx_credit_ledger_order_id
    ON public.credit_ledger(order_id)
    WHERE order_id IS NOT NULL;

CREATE INDEX IF NOT EXISTS idx_credit_ledger_type
    ON public.credit_ledger(type);
